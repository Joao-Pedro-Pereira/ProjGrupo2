    private void guardar(){

        if (txtid.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Introduza o codigo do Hospital!");            
            txtid.requestFocus();
            return;
        }
        if (txtnome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Introduza o nome do Hospital!");            
            txtnome.requestFocus();
            return;
        }
        if (txtlocalidade.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Introduza o local do Hospital!");            
            txtlocalidade.requestFocus();
            return;
        }else{
          hospital = new Hospital(txtnome.getText(),txtlocalidade.getText(),txtid.getText());
            try {
                sistema.getListaHospitais().adicionar(hospital);
            } catch (ListaHospitais.HospitalDuplicadoException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
                return;
            } 
        } 
        JOptionPane.showMessageDialog(this, "Registo guardado com sucesso.");
        fechar();  
    }
    private void fechar(){
        dispose();
        
    }
